

public class perro
{
   String nombre;
   String raza;
   int edad;
   String color;
   public String setnombre(String nombre){
    this.nombre= nombre;
    return nombre;
    }
    public String getnombre(String nombre){
    return nombre;
    }
   public String setraza(String raza){
    this.raza= raza;
    return raza;
    }  
   public String getraza(String raza){
    return raza;
    }
    public int setedad(int edad ){
    this.edad=edad;
    return edad;
    }
    public int getedad(int edad){
    return edad;
    }
    public String setcolor(String color){
    this.color=color;
    return color;
    }
    public String getcolor(String color){
    return color;
    }
}
